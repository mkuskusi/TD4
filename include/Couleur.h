#ifndef TD4_COULEUR_H
#define TD4_COULEUR_H

/**
 * @brief a enum class of color for candle.
 */
enum class Couleur {
    /**
     * @brief red color meaning the close price higher than open price.
     */
    rouge,
    /**
     * @brief green color meaning the close price lower than open price.
     */
    vert
};


#endif //TD4_COULEUR_H
